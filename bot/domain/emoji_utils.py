def normalize_emoji(emoji: str) -> str:
    """Убирает U+FE0F (variation selector-16) из эмодзи-строки.

    Telegram-клиенты могут отправлять одни и те же эмодзи как с U+FE0F,
    так и без него. Например:
        ❤️  = U+2764 U+FE0F
        ❤   = U+2764
    Визуально это одно и то же, но строки разные.
    Нормализация позволяет сравнивать их единообразно.
    """
    return emoji.replace("\ufe0f", "")
